  form.addEventListener("submit", function (e) {
    const nom = document.getElementById("nom").value.trim();
    const prenom = document.getElementById("prenom").value.trim();
    const email = document.getElementById("email").value.trim();
    const mdp = document.getElementById("mdp").value;

    const nomPrenomRegex = /^[A-Z][a-zA-Z]{2,}$/;
    const emailRegex = /^[^@]+@[^@]+\.[a-z]{2,}$/i;
    const passwordMinLength = 6;

    let erreur = "";

    if (!nomPrenomRegex.test(nom)) {
      erreur += "Le nom doit commencer par une majuscule et contenir au moins 3 lettres.\n";
    }

    if (!nomPrenomRegex.test(prenom)) {
      erreur += "Le prénom doit commencer par une majuscule et contenir au moins 3 lettres.\n";
    }

    if (!emailRegex.test(email)) {
      erreur += "L'adresse email n'est pas valide.\n";
    }

    if (mdp.length < passwordMinLength) {
      erreur += "Le mot de passe doit contenir au moins 6 caractères.\n";
    }

    if (erreur) {
      e.preventDefault();
      alert(erreur);
    }
  });
